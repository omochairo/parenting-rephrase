function generateHugeData(): RephraseItem[] {
    let idCounter = 1;
    const items: RephraseItem[] = [];

    masterData.forEach(template => {
        // For each "Before" phrase, create a separate item
        template.b.forEach(beforePhrase => {
            items.push({
                id: idCounter++,
                category: template.c,
                situation: template.s,
                before: beforePhrase,
                after: template.a, // 3-type object
                reason: template.r,
                tags: template.t,
                targetAges: template.age,
                moods: template.m
            });
        });

        // Add a generic one if not covered? No, the loop covers all.
        // We can add slight randomization to ID or category if needed, but this is fine.
    });

    return items;
}

